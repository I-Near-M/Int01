    private void cargarTabla() {

        limpiarTabla();
        List<Alumno> listaAlumnos = alumnoController.listarAlumnos(); // Reemplaza con tu instancia del DAO
        modelo = (DefaultTableModel) jTableAlumnos.getModel(); // Reemplaza TableCliente con el nombre de tu JTable
        Object[] ob = new Object[12]; // Ajusta el tamaño del array según los atributos de Alumno

        for (int i = 0; i < listaAlumnos.size(); i++) {
            Alumno alumno = listaAlumnos.get(i);
            ob[0] = alumno.getDni();
            ob[1] = alumno.getNombre();
            ob[2] = alumno.getApellidoPaterno();
            ob[3] = alumno.getApellidoMaterno();
            ob[4] = alumno.getGenero();
            ob[5] = alumno.getFechaNacimiento();
            ob[6] = alumno.getDireccion();
            ob[7] = alumno.getTelefono();
            ob[8] = gradoController.buscarGrado(alumno.getGradoId()).getNombre();
            ob[9] = seccionController.buscarSeccion(alumno.getSeccionId()).getNombre();
            ob[10] = apoderadoController.buscarApoderado(alumno.getApoderadoId()).getNombre();
            modelo.addRow(ob);
        }
        jTableAlumnos.setModel(modelo);
    }

    private void limpiarTabla() {
        modelo = (DefaultTableModel) jTableAlumnos.getModel();
        int rowCount = modelo.getRowCount();

        for (int i = rowCount - 1; i >= 0; i--) {
            modelo.removeRow(i);
        }
    }

