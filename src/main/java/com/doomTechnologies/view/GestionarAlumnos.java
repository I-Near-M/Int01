    private void jButtonRegistrarMatriculaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistrarMatriculaActionPerformed

// En el método jButtonRegistrarMatriculaActionPerformed
String dni = jTxtDni.getText();
String nombre = jTxtNombre.getText();
String apPaterno = jTxtApPaterno.getText();
String apMaterno = jTxtApMaterno.getText();
String direccion = jTxtDireccion.getText();
String telefonoAlumno = jTxtTelefonoAlumno.getText();
char genero = jComboBoxGenero.getSelectedItem().toString().charAt(0);
Date fechaNacimiento = jDateChooserFechaNac.getDate();
int gradoId = jComboBoGrado.getSelectedIndex() + 1;
int seccionId = jComboBoxSeccion.getSelectedIndex() + 1;

// Datos del apoderado
String nombreApoderado = jTxtNombreApoderado.getText();
String apPaternoApoderado = jTxtApPaternoApoderado.getText();
String apMaternoApoderado = jTxtApMaternoApoderado.getText();
String telefonoApoderado = jTxtTelefonoApoderado.getText();

Apoderado nuevoApoderado = Apoderado.builder()
        .id(0)
        .nombre(nombreApoderado)
        .apellidoPaterno(apPaternoApoderado)
        .apellidoMaterno(apMaternoApoderado)
        .telefono(telefonoApoderado)
        .build();

if (Validar.isNotEmpty(nombreApoderado) && Validar.isNotEmpty(apPaternoApoderado) && Validar.isNotEmpty(apMaternoApoderado) && Validar.isNotEmpty(telefonoApoderado)) {
    int apoderadoId = apoderadoController.registrarApoderadoRetornaID(nuevoApoderado);

    if (fechaNacimiento != null) {
        // Crear el usuario
        Usuario nuevoUsuario = Usuario.builder()
                .username(dni)
                .password(dni)
                .email("")
                .rolId(3)
                .build();
        if (usuarioController.regitrarUsuario(nuevoUsuario)) {
            // Crear el alumno con el ID del apoderado y el ID del usuario
            int idUsuario = usuarioController.buscarUsuarioPorUsername(nuevoUsuario.getUsername()).getId();
            Alumno alumno = Alumno.builder()
                    .dni(dni)
                    .nombre(nombre)
                    .apellidoPaterno(apPaterno)
                    .apellidoMaterno(apMaterno)
                    .genero(genero)
                    .fechaNacimiento(fechaNacimiento)
                    .direccion(direccion)
                    .telefono(telefonoAlumno)
                    .gradoId(gradoId)
                    .seccionId(seccionId)
                    .apoderadoId(apoderadoId)
                    .usuarioId(idUsuario)
                    .build();
            alumnoController.registrarAlumno(alumno);
            JOptionPane.showMessageDialog(null, "Alumno registrado");
            limpiarCampos();
        } else {
            JOptionPane.showMessageDialog(this, "El usuario ya existe.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } else {
        JOptionPane.showMessageDialog(this, "Por favor, seleccione una fecha de nacimiento.", "Error", JOptionPane.ERROR_MESSAGE);
    }
} else {
    JOptionPane.showMessageDialog(this, "Datos del apoderado no válidos.", "Error", JOptionPane.ERROR_MESSAGE);
}
    }//GEN-LAST:event_jButtonRegistrarMatriculaActionPerformed

