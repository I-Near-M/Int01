    private void jButtonBuscarDniAlumnoEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarDniAlumnoEditActionPerformed
        String dni = jTxtDniEdit.getText();
        Alumno alumno = alumnoController.buscarAlumno(dni);

        if (alumno != null) {
            jTxtNombreEdit.setText(alumno.getNombre());
            jTxtApPaternoEdit.setText(alumno.getApellidoPaterno());
            jTxtApMaternoEdit.setText(alumno.getApellidoMaterno());
            char genero = alumno.getGenero();
            if (genero == 'M') {
                jComboBoxGeneroEdit.setSelectedIndex(0);
            } else if (genero == 'F') {
                jComboBoxGeneroEdit.setSelectedIndex(1);
            }
            jDateChooserFechaNacEdit.setDate(alumno.getFechaNacimiento());
            jTxtDireccionEdit.setText(alumno.getDireccion());
            jTxtTelefonoAlumnoEdit.setText(alumno.getTelefono());
            jComboBoGradoEdit.setSelectedIndex(alumno.getGradoId() - 1);
            jComboBoxSeccionEdit.setSelectedIndex(alumno.getSeccionId() - 1);

            Apoderado apoderado = apoderadoController.buscarApoderado(alumno.getApoderadoId());
            if (apoderado != null) {
                jTxtNombreApoderadoEdit.setText(apoderado.getNombre());
                jTxtApPaternoApoderadoEdit.setText(apoderado.getApellidoPaterno());
                jTxtApMaternoApoderadoEdit.setText(apoderado.getApellidoMaterno());
                jTxtTelefonoApoderadoEdit.setText(apoderado.getTelefono());
            } else {
                JOptionPane.showMessageDialog(this, "Apoderado no encontrado.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Alumno no encontrado.", "Error", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_jButtonBuscarDniAlumnoEditActionPerformed

