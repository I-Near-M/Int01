    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        
        try (Connection connection = ConexionDB.getInstance().getConnection()) {
            if (connection != null && !connection.isClosed()) {
                log.info("Conexión a la base de datos establecida exitosamente.");
            } else {
                log.error("No se pudo establecer la conexión a la base de datos.");
                JOptionPane.showMessageDialog(this, "Error al conectar con la base de datos", "Error", JOptionPane.ERROR_MESSAGE);
                System.exit(1);
            }
        } catch (SQLException e) {
            log.error("Error al conectar con la base de datos", e);
            JOptionPane.showMessageDialog(this, "Error al conectar con la base de datos", "Error", JOptionPane.ERROR_MESSAGE);
            System.exit(1);
        }
        
    }//GEN-LAST:event_formWindowOpened


