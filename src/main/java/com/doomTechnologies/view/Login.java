    private void jButtonIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIngresarActionPerformed

        String usuario;
        char[] passwordChars;

        if (jTextFieldNombre.getText().isEmpty() || jPasswordFieldContraseña.getPassword().length == 0) {
            JOptionPane.showMessageDialog(null, "Ingrese usuario y contraseña");
            return;
        }
        usuario = jTextFieldNombre.getText();
        passwordChars = jPasswordFieldContraseña.getPassword();
        String password = new String(passwordChars);    
        Arrays.fill(passwordChars, '0');
//      Validar.isPasswordStrong(password);
        if (loginController.validarUsuario(usuario, password)) {
            JOptionPane.showMessageDialog(null, "Bienvenido");
            log.warn("Usuario {} ha ingresado al sistema" + usuario);

            //Abrir ventana principal
            java.awt.EventQueue.invokeLater(new Runnable() {

                public void run() {

                    Principal principal = new Principal();
                    principal.setVisible(true);
                    principal.setLocationRelativeTo(null);
                    //principal.setLocation(null);

                }

            });
            this.dispose();

        } else {
            JOptionPane.showMessageDialog(null, "Usuario o contraseña incorrectos");
            log.info("Usuario ha ingresado una contraseña incorrecta"+ " " + usuario);
        }


    }//GEN-LAST:event_jButtonIngresarActionPerformed

